double add(double v1, double v2){
    return v1 + v2;
}

double sub(double v1, double v2){
    return v1 - v2;
}

double mul(double v1, double v2){
    return v1 * v2;
}

double div(double v1, double v2){
    return v1 / v2;
}

double mod(double v1, double v2){
    return (double)(int(v1) % (int)(v2));
}
